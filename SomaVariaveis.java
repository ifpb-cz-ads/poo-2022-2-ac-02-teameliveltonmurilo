public class SomaVariaveis {
    public static void main(String[] args){
        int primeiroNumero = 4;
        int segundoNumero = 7;
        int terceiroNumero = 34;
        int soma = primeiroNumero + segundoNumero + terceiroNumero;
        int media = soma/3;
        System.out.println("Soma dos números = " + primeiroNumero + " + " + segundoNumero + " + " + terceiroNumero + " = " + soma);
        System.out.println("Média dos números = " + media);
    }
}
